const RCON = new Uint8Array([0x8d,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36]);

const NK = 8; // 256-bit key
const NR = 14; // rounds
const NB = 4;

function xtime(x: number): number {
  return ((x << 1) ^ (((x >> 7) & 1) * 0x1b)) & 0xff;
}

function mul(x: number, y: number): number {
  let r = 0;
  for (let i = 0; i < 8; i++) {
    if (y & 1) r ^= x;
    const hi = x & 0x80;
    x = (x << 1) & 0xff;
    if (hi) x ^= 0x1b;
    y >>= 1;
  }
  return r & 0xff;
}

/** Expand a 32-byte key into the (NR+1)*16 = 240-byte round-key schedule. */
function expandKey(key: Uint8Array): Uint8Array {
  if (key.length !== 32) throw new Error('AES-256 key must be 32 bytes');
  const rk = new Uint8Array(240);
  const t = new Uint8Array(4);
  for (let i = 0; i < NK; i++) for (let j = 0; j < 4; j++) rk[i * 4 + j] = key[i * 4 + j];
  for (let i = NK; i < NB * (NR + 1); i++) {
    const k = (i - 1) * 4;
    for (let j = 0; j < 4; j++) t[j] = rk[k + j];
    if (i % NK === 0) {
      const tmp = t[0];
      t[0] = t[1];
      t[1] = t[2];
      t[2] = t[3];
      t[3] = tmp;
      for (let j = 0; j < 4; j++) t[j] = SBOX[t[j]];
      t[0] ^= RCON[i / NK];
    } else if (i % NK === 4) {
      for (let j = 0; j < 4; j++) t[j] = SBOX[t[j]];
    }
    const j2 = i * 4;
    const k2 = (i - NK) * 4;
    for (let j = 0; j < 4; j++) rk[j2 + j] = rk[k2 + j] ^ t[j];
  }
  return rk;
}

function addRoundKey(s: Uint8Array, rk: Uint8Array, off: number): void {
  for (let i = 0; i < 16; i++) s[i] ^= rk[off + i];
}

// state is column-major: s[c*4 + r]
function subShift(s: Uint8Array): void {
  const t = new Uint8Array(16);
  for (let i = 0; i < 16; i++) t[i] = SBOX[s[i]];
  const o = new Uint8Array(16);
  for (let c = 0; c < 4; c++) for (let r = 0; r < 4; r++) o[c * 4 + r] = t[(((c + r) & 3) * 4) + r];
  s.set(o);
}

function invSubShift(s: Uint8Array): void {
  const t = new Uint8Array(16);
  for (let i = 0; i < 16; i++) t[i] = RSBOX[s[i]];
  const o = new Uint8Array(16);
  for (let c = 0; c < 4; c++) for (let r = 0; r < 4; r++) o[c * 4 + r] = t[(((c - r + 4) & 3) * 4) + r];
  s.set(o);
}

function mixColumns(s: Uint8Array): void {
  for (let c = 0; c < 4; c++) {
    const p = c * 4;
    const a = s[p], b = s[p + 1], d = s[p + 2], e = s[p + 3];
    const tt = a ^ b ^ d ^ e;
    s[p] ^= tt ^ xtime(a ^ b);
    s[p + 1] ^= tt ^ xtime(b ^ d);
    s[p + 2] ^= tt ^ xtime(d ^ e);
    s[p + 3] ^= tt ^ xtime(e ^ a);
  }
}

function invMixColumns(s: Uint8Array): void {
  for (let c = 0; c < 4; c++) {
    const p = c * 4;
    const a = s[p], b = s[p + 1], d = s[p + 2], e = s[p + 3];
    s[p] = mul(a, 14) ^ mul(b, 11) ^ mul(d, 13) ^ mul(e, 9);
    s[p + 1] = mul(a, 9) ^ mul(b, 14) ^ mul(d, 11) ^ mul(e, 13);
    s[p + 2] = mul(a, 13) ^ mul(b, 9) ^ mul(d, 14) ^ mul(e, 11);
    s[p + 3] = mul(a, 11) ^ mul(b, 13) ^ mul(d, 9) ^ mul(e, 14);
  }
}

function encryptBlock(rk: Uint8Array, s: Uint8Array): void {
  addRoundKey(s, rk, 0);
  for (let r = 1; r < NR; r++) {
    subShift(s);
    mixColumns(s);
    addRoundKey(s, rk, r * 16);
  }
  subShift(s);
  addRoundKey(s, rk, NR * 16);
}

function decryptBlock(rk: Uint8Array, s: Uint8Array): void {
  addRoundKey(s, rk, NR * 16);
  for (let r = NR - 1; r > 0; r--) {
    invSubShift(s);
    addRoundKey(s, rk, r * 16);
    invMixColumns(s);
  }
  invSubShift(s);
  addRoundKey(s, rk, 0);
}

// ───────────────────────── CBC + PKCS7 ─────────────────────────

/** AES-256-CBC encrypt with PKCS7 padding. `data` is plaintext bytes; returns ciphertext bytes. */
export function aesCbcEncrypt(key: Uint8Array, iv: Uint8Array, data: Uint8Array): Uint8Array {
  const rk = expandKey(key);
  const padLen = 16 - (data.length % 16); // PKCS7: always 1..16 bytes of padding
  const buf = new Uint8Array(data.length + padLen);
  buf.set(data);
  buf.fill(padLen, data.length);

  const prev = new Uint8Array(iv); // copy
  const block = new Uint8Array(16);
  for (let off = 0; off < buf.length; off += 16) {
    for (let i = 0; i < 16; i++) block[i] = buf[off + i] ^ prev[i];
    encryptBlock(rk, block);
    buf.set(block, off);
    prev.set(block);
  }
  return buf;
}

/** AES-256-CBC decrypt, strips PKCS7. Returns plaintext bytes. Throws on malformed input. */
export function aesCbcDecrypt(key: Uint8Array, iv: Uint8Array, ct: Uint8Array): Uint8Array {
  if (ct.length === 0 || ct.length % 16 !== 0) {
    throw new Error('ciphertext must be a non-empty multiple of 16 bytes');
  }
  const rk = expandKey(key);
  const out = new Uint8Array(ct.length);
  const prev = new Uint8Array(iv);
  const cipher = new Uint8Array(16);
  const block = new Uint8Array(16);
  for (let off = 0; off < ct.length; off += 16) {
    cipher.set(ct.subarray(off, off + 16));
    block.set(cipher);
    decryptBlock(rk, block);
    for (let i = 0; i < 16; i++) out[off + i] = block[i] ^ prev[i];
    prev.set(cipher);
  }
  // strip PKCS7
  const pad = out[out.length - 1];
  if (pad >= 1 && pad <= 16) {
    return out.subarray(0, out.length - pad);
  }
  return out; // not padded as expected — return as-is (matches the C++ fallback)
}

// ───────────────────────── hex + text helpers ─────────────────────────

const HEX = '0123456789abcdef';

export function toHex(bytes: Uint8Array): string {
  let s = '';
  for (let i = 0; i < bytes.length; i++) {
    s += HEX[bytes[i] >> 4];
    s += HEX[bytes[i] & 15];
  }
  return s;
}

export function fromHex(hex: string): Uint8Array {
  if (hex.length % 2 !== 0) throw new Error('odd-length hex string');
  const out = new Uint8Array(hex.length / 2);
  for (let i = 0; i < out.length; i++) {
    const hi = parseInt(hex[i * 2], 16);
    const lo = parseInt(hex[i * 2 + 1], 16);
    if (Number.isNaN(hi) || Number.isNaN(lo)) throw new Error('invalid hex');
    out[i] = (hi << 4) | lo;
  }
  return out;
}

// We deliberately avoid TextEncoder/TextDecoder: they are not guaranteed on every Hermes
// build. Our synthetic question JSON is pure ASCII, so byte<->char is 1:1.
export function utf8Encode(str: string): Uint8Array {
  const out = new Uint8Array(str.length);
  for (let i = 0; i < str.length; i++) out[i] = str.charCodeAt(i) & 0xff;
  return out;
}

export function utf8Decode(bytes: Uint8Array): string {
  let s = '';
  for (let i = 0; i < bytes.length; i++) s += String.fromCharCode(bytes[i]);
  return s;
}

/**
 * FIPS-197 AES-256 known-answer test (App. C.3): key=000102…1f, pt=00112233…eeff
 * → ct=8ea2b7ca516745bfeafc49904b496089. Proves this JS port matches the native C++ core.
 */
export function selfTestAES(): boolean {
  const key = new Uint8Array(32);
  for (let i = 0; i < 32; i++) key[i] = i;
  const pt = new Uint8Array([
    0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x88, 0x99, 0xaa, 0xbb, 0xcc, 0xdd, 0xee, 0xff,
  ]);
  const rk = expandKey(key);
  const blk = new Uint8Array(pt);
  encryptBlock(rk, blk);
  if (toHex(blk) !== '8ea2b7ca516745bfeafc49904b496089') return false;
  decryptBlock(rk, blk);
  for (let i = 0; i < 16; i++) if (blk[i] !== pt[i]) return false;
  return true;
}
