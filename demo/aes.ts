/**
 * Self-contained AES-256-CBC + PKCS7 + hex, in pure TypeScript — NO external deps.
 *
 * This is a faithful JS port of the demo's native C++ core
 * (`matiks-engine/src/native/cpp/matiks_decrypt.cpp`, vendored tiny-AES, public domain),
 * so the JS path (Button A) and the native Nitro path (Button B) decrypt *identical* data
 * with *identical* semantics. The point of this file is to be the slow thing: running this
 * decrypt synchronously on the JS thread is exactly what freezes Matiks at match-start
 * (~2.5 s on a 32-bit Hermes build — see report 13-native-decrypt-ondevice.md).
 *
 * It passes the FIPS-197 AES-256 known-answer test (see `selfTestAES` below). It is NOT
 * meant to be fast or constant-time — it is the "current approach" we are arguing against.
 */

