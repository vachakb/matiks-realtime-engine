static const uint8_t Rcon[11] = {0x8d,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36};

static inline uint8_t xtime(uint8_t x){ return (uint8_t)((x<<1) ^ (((x>>7)&1)*0x1b)); }
static uint8_t mul(uint8_t x, uint8_t y){
  uint8_t r=0;
  for(int i=0;i<8;i++){ if(y&1) r^=x; uint8_t hi=x&0x80; x<<=1; if(hi) x^=0x1b; y>>=1; }
  return r;
}

struct Ctx { uint8_t rk[240]; };  // (Nr+1)*16 = 240 for AES-256

static void expand(Ctx& c, const uint8_t key[32]){
  const int Nk=8, Nr=14, Nb=4;
  uint8_t* rk=c.rk; uint8_t t[4];
  for(int i=0;i<Nk;i++) for(int j=0;j<4;j++) rk[i*4+j]=key[i*4+j];
  for(int i=Nk;i<Nb*(Nr+1);i++){
    int k=(i-1)*4; for(int j=0;j<4;j++) t[j]=rk[k+j];
    if(i%Nk==0){ uint8_t tmp=t[0]; t[0]=t[1];t[1]=t[2];t[2]=t[3];t[3]=tmp;
      for(int j=0;j<4;j++) t[j]=sbox[t[j]]; t[0]^=Rcon[i/Nk]; }
    else if(i%Nk==4){ for(int j=0;j<4;j++) t[j]=sbox[t[j]]; }
    int j2=i*4, k2=(i-Nk)*4; for(int j=0;j<4;j++) rk[j2+j]=rk[k2+j]^t[j];
  }
}

static void addRoundKey(uint8_t st[16], const uint8_t* rk){ for(int i=0;i<16;i++) st[i]^=rk[i]; }
static void invSubShift(uint8_t s[16]){
  // InvSubBytes + InvShiftRows combined on column-major state s[c*4+r]
  uint8_t t[16];
  for(int i=0;i<16;i++) t[i]=rsbox[s[i]];
  // InvShiftRows: row r shifted right by r
  uint8_t o[16];
  for(int c=0;c<4;c++) for(int r=0;r<4;r++) o[c*4+r]=t[((c - r + 4)&3)*4 + r];
  memcpy(s,o,16);
}
static void invMixColumns(uint8_t s[16]){
  for(int c=0;c<4;c++){ uint8_t* p=s+c*4; uint8_t a=p[0],b=p[1],d=p[2],e=p[3];
    p[0]=mul(a,14)^mul(b,11)^mul(d,13)^mul(e,9);
    p[1]=mul(a,9)^mul(b,14)^mul(d,11)^mul(e,13);
    p[2]=mul(a,13)^mul(b,9)^mul(d,14)^mul(e,11);
    p[3]=mul(a,11)^mul(b,13)^mul(d,9)^mul(e,14);
  }
}
static void subShift(uint8_t s[16]){
  uint8_t t[16]; for(int i=0;i<16;i++) t[i]=sbox[s[i]];
  uint8_t o[16]; for(int c=0;c<4;c++) for(int r=0;r<4;r++) o[c*4+r]=t[((c + r)&3)*4 + r];
  memcpy(s,o,16);
}
static void mixColumns(uint8_t s[16]){
  for(int c=0;c<4;c++){ uint8_t* p=s+c*4; uint8_t a=p[0],b=p[1],d=p[2],e=p[3];
    uint8_t t=a^b^d^e;
    p[0]^=t^xtime((uint8_t)(a^b)); p[1]^=t^xtime((uint8_t)(b^d));
    p[2]^=t^xtime((uint8_t)(d^e)); p[3]^=t^xtime((uint8_t)(e^a));
  }
}
static void encryptBlock(const Ctx& c, uint8_t s[16]){
  addRoundKey(s,c.rk);
  for(int r=1;r<14;r++){ subShift(s); mixColumns(s); addRoundKey(s,c.rk+r*16); }
  subShift(s); addRoundKey(s,c.rk+14*16);
}
static void decryptBlock(const Ctx& c, uint8_t s[16]){
  addRoundKey(s,c.rk+14*16);
  for(int r=13;r>0;r--){ invSubShift(s); addRoundKey(s,c.rk+r*16); invMixColumns(s); }
  invSubShift(s); addRoundKey(s,c.rk);
}
// CBC decrypt in place; returns plaintext length after stripping PKCS7
static size_t cbcDecrypt(const Ctx& c, const uint8_t iv[16], uint8_t* buf, size_t len){
  if(len==0 || (len & 15)) return 0;   // reject empty / non-16-aligned ciphertext (no OOB read)
  uint8_t prev[16]; memcpy(prev,iv,16);
  for(size_t off=0; off<len; off+=16){
    uint8_t cipher[16]; memcpy(cipher,buf+off,16);
    decryptBlock(c, buf+off);
    for(int i=0;i<16;i++) buf[off+i]^=prev[i];
    memcpy(prev,cipher,16);
  }
  if(len==0) return 0;
  uint8_t pad=buf[len-1]; if(pad==0||pad>16) return len; // not padded as expected
  return len-pad;
}
static void cbcEncrypt(const Ctx& c, const uint8_t iv[16], uint8_t* buf, size_t len){
  uint8_t prev[16]; memcpy(prev,iv,16);
  for(size_t off=0; off<len; off+=16){
    for(int i=0;i<16;i++) buf[off+i]^=prev[i];
    encryptBlock(c, buf+off);
    memcpy(prev,buf+off,16);
  }
}
