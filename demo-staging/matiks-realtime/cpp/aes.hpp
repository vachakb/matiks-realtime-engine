static inline std::string hex(const uint8_t* p, size_t n){ static const char* h="0123456789abcdef"; std::string s; s.reserve(n*2); for(size_t i=0;i<n;i++){ s+=h[p[i]>>4]; s+=h[p[i]&15]; } return s; }
static inline bool fromHex(const std::string& s, std::vector<uint8_t>& out){ if(s.size()%2) return false; out.clear(); for(size_t i=0;i<s.size();i+=2){ auto v=[](char c)->int{ if(c>='0'&&c<='9')return c-'0'; if(c>='a'&&c<='f')return c-'a'+10; if(c>='A'&&c<='F')return c-'A'+10; return -1;}; int hi=v(s[i]),lo=v(s[i+1]); if(hi<0||lo<0) return false; out.push_back((uint8_t)(hi*16+lo)); } return true; }

// ───────────────────────── FIPS-197 known-answer test ─────────────────────────
// Same vector as matiks_decrypt.cpp. Call once (e.g. behind std::call_once) to prove
// the crypto is correct on THIS toolchain before trusting any decrypt result.
static inline bool aesSelfTest(){
  uint8_t key[32]; for(int i=0;i<32;i++) key[i]=(uint8_t)i; // 000102...1f
  uint8_t pt[16]={0x00,0x11,0x22,0x33,0x44,0x55,0x66,0x77,0x88,0x99,0xaa,0xbb,0xcc,0xdd,0xee,0xff};
  const char* expect="8ea2b7ca516745bfeafc49904b496089";
  aes::Ctx c; aes::expand(c,key);
  uint8_t blk[16]; memcpy(blk,pt,16); aes::encryptBlock(c,blk);
  if(hex(blk,16)!=expect) return false;
  aes::decryptBlock(c,blk);
  return memcmp(blk,pt,16)==0;
}
